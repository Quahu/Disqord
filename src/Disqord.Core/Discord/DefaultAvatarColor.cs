namespace Disqord;

/// <summary>
///     Represents a default Discord user avatar value.
/// </summary>
public enum DefaultAvatarColor : byte
{
    /// <summary>
    ///     The blurple default user avatar.
    /// </summary>
    Blurple = 0,

    /// <summary>
    ///     The gray default user avatar.
    /// </summary>
    Gray = 1,

    /// <summary>
    ///     The green default user avatar.
    /// </summary>
    Green = 2,

    /// <summary>
    ///     The yellow default user avatar.
    /// </summary>
    Yellow = 3,

    /// <summary>
    ///     The red default user avatar.
    /// </summary>
    Red = 4,

    /// <summary>
    ///     The fuchsia default user avatar.
    /// </summary>
    Fuchsia = 5
}